# Colon : ka kya role hai?

# Python me colon (:) ka matlab hai:

# "Ye block yaha se start ho raha hai"

# Colon ke baad indentation (tab/space) aana chahiye.

if True:
    print("Hello")   # colon batata hai yaha se block start hoga


# Same in loops:

for i in range(3):
    print(i)   # colon ke baad wali line indented honi chahiye


# Aur slicing me bhi colon use hota hai:

text = "python"
print(text[0:4])   # "pyth"  (index 0 se lekar 3 tak)
print(text[::-1])  # "nohtyp" (step -1 means reverse)
