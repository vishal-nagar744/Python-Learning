# 🔹 1. Django Kya Hai?

# Django ek high-level Python web framework hai jo:
# ✅ Rapid development ke liye bana hai (boilerplate kam likhna padta hai).
# 🔒 Secure hai (authentication, CSRF protection built-in).
# 🗄️ ORM ke sath aata hai (SQL likhne ki zarurat nahi, Python objects se database manage karte ho).
# 📦 Admin panel free me milta hai.
# 🌍 Full-stack support (backend + templates + static files).

# Use cases:

# Web apps (Instagram, Pinterest Django par bane the 💯)
# REST APIs (Django REST Framework ke sath)
# Admin dashboards